def get_trends_country(country):

    url = f"https://trends.google.com/trending/rss?geo={country}"
    response = requests.get(url, timeout=10)

    root = ET.fromstring(response.content)

    ns = {'ht': 'https://trends.google.com/trending/rss'}

    data = []

    for pos, item in enumerate(root.findall('./channel/item'), start=1):

        keyword = item.find('title').text
        traffic = item.find('ht:approx_traffic', ns).text
        date = item.find('pubDate').text
        image = item.find('ht:picture', ns).text

        news = item.find('ht:news_item', ns)

        if news is not None:
            news_title = news.find('ht:news_item_title', ns).text
            news_url = news.find('ht:news_item_url', ns).text
        else:
            news_title = None
            news_url = None

        data.append([
            country, pos, keyword, traffic, date,
            news_title, news_url, image
        ])

    return pd.DataFrame(data, columns=[
        'country','position','keyword','traffic','date',
        'news_title','news_url','image_url'
    ])


