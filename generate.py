# Collect all unique non-null texts, translate in one batch per chunk,
# then map back — avoids redundant API calls for repeated terms.

def translate_series(series, src='auto', dest='pt', chunk=500):
    """Translate a pandas Series to PT-BR, batching by chunk size."""
    translator = GoogleTranslator(source=src, target=dest)
    unique_texts = series.dropna().unique().tolist()
    translation_map = {}

    for i in range(0, len(unique_texts), chunk):
        batch = unique_texts[i:i + chunk]
        for text in batch:
            try:
                translated = translator.translate(str(text))
                translation_map[text] = translated if translated else text
            except Exception:
                translation_map[text] = text   # keep original on failure
        time.sleep(0.3)  # be polite to the API

    return series.map(lambda x: translation_map.get(x, x) if pd.notna(x) else x)

